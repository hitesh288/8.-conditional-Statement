public class Swich_op {
    public static void main(String[] args) {
        int Num = 1;
        switch (Num) {
            case 1:
                System.out.println("You Are Choose Number 1");
            break;
            case 2:
                System.out.println("You Are Choose Number 2");
            break;
            case 3:
            System.out.println("You Are Choose Number 3");
            break;
        
            default:
            System.out.println("You Are Choose wrong Number");
                break;
        }
    }
}
